package com.example.demo.dto;

public class ActorDto {
	public String actorName;
	public String actorSex;
	public String actorDob;
	public String actorBio;
	public int actorId;
	
	public int getActorId() {
		return actorId;
	}
	public void setActorId(int actorId) {
		this.actorId = actorId;
	}
	public String getActorName() {
		return actorName;
	}
	public void setActorName(String actorName) {
		this.actorName = actorName;
	}
	public String getActorSex() {
		return actorSex;
	}
	public void setActorSex(String actorSex) {
		this.actorSex = actorSex;
	}
	public String getActorDob() {
		return actorDob;
	}
	public void setActorDob(String actorDob) {
		this.actorDob = actorDob;
	}
	public String getActorBio() {
		return actorBio;
	}
	public void setActorBio(String actorBio) {
		this.actorBio = actorBio;
	}

}
